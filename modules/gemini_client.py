import os
import logging
from dotenv import load_dotenv
from google import genai
from google.genai import types

load_dotenv()
logger = logging.getLogger(__name__)

# Отримуємо API ключ зі змінних середовища
GEMINI_API_KEY = os.getenv("GEMINI_API_KEY")
if not GEMINI_API_KEY:
    raise ValueError("GEMINI_API_KEY не знайдено в .env")

# Ініціалізуємо клієнта
client = genai.Client(api_key=GEMINI_API_KEY)

# СИСТЕМНИЙ ПРОМПТ (керує поведінкою моделі для стиснення)
SYSTEM_PROMPT = """
Ти — WikiBot, стислий та точний ШІ-асистент.

## ТВОЯ РОЛЬ
Ти допомагаєш користувачам отримувати короткі, зрозумілі відповіді на їхні питання.

## ВАЖЛИВІ ПРАВИЛА (НЕ ПОРУШУЙ!)

1. ВІДПОВІДАЙ ТІЛЬКИ НА УКРАЇНСЬКІЙ МОВІ.
2. МАКСИМУМ 3-4 РЕЧЕННЯ НА ВІДПОВІДЬ — НІЯКОЇ "ВОДИ"!
3. ЯКЩО НЕ ЗНАЄШ ТОЧНОЇ ВІДПОВІДІ — СКАЖИ "Не знайшов точної інформації" — НЕ ВИГАДУЙ!
4. НЕ ДОДАВАЙ ВІТАННЯ, ПРОЩАННЯ, ЕМОДЗІ ТА ЗАЙВІ СЛОВА.
5. ВИКОРИСТОВУЙ ПРОСТУ, ЗРОЗУМІЛУ ДЛЯ ПОЧАТКІВЦЯ МОВУ.

## ФОРМАТ ВІДПОВІДІ
Відповідай ТІЛЬКИ текстом у такому стилі:
[Одна стисла відповідь на питання без зайвих конструкцій]

## ПРИКЛАДИ ДОБРОЇ ВІДПОВІДІ

ПИТАННЯ: "Що таке Python?"
ВІДПОВІДЬ: Python — це мова програмування, яку легко вивчати. Вона широко використовується для штучного інтелекту, аналізу даних та веб-розробки.

ПИТАННЯ: "Що таке ШІ?"
ВІДПОВІДЬ: Штучний інтелект — це здатність комп'ютерів імітувати людське мислення. Він використовується для розпізнавання зображень, голосу та прийняття рішень.

ПИТАННЯ: "Що таке RAG?"
ВІДПОВІДЬ: RAG — це метод, коли ШІ спочатку шукає відповідь у власній базі знань, а потім генерує відповідь на основі знайденого. Це зменшує кількість помилок (галюцинацій).

## ВХІДНІ ДАНІ ДЛЯ ОБРОБКИ
Питання користувача: {{QUESTION}}
Результати пошуку в інтернеті (якщо є): {{SEARCH_RESULTS}}

## ІНСТРУКЦІЯ
Використовуючи результати пошуку (якщо вони є), дай стислу відповідь на питання користувача. Якщо результатів пошуку недостатньо — чесно скажи, що не знаєш.
"""


def summarize_with_gemini(question: str, search_results: str) -> str:
    """
    Надсилає питання та результати пошуку до Gemini з системним промптом,
    отримує стислу відповідь.
    """
    if not search_results or len(search_results.strip()) == 0:
        return "Не знайшов точної інформації в інтернеті. Спробуйте переформулювати питання."

    # Формуємо користувацький промпт з даними
    user_content = f"""
<user_question>
{question}
</user_question>

<search_results>
{search_results}
</search_results>

ІНСТРУКЦІЯ: На основі виключно результатів пошуку дай стислу відповідь (3-4 речення) українською мовою.
"""
    try:
        response = client.models.generate_content(
            model="gemini-2.0-flash",  # можна gemini-1.5-flash
            contents=user_content,
            config=types.GenerateContentConfig(
                system_instruction=SYSTEM_PROMPT,
                temperature=0.3,
                max_output_tokens=300
            )
        )
        return response.text.strip()
    except Exception as e:
        logger.error(f"Помилка Gemini API: {e}")
        return "Вибачте, сталася технічна помилка при зверненні до ШІ. Спробуйте пізніше."
import os
import logging
from dotenv import load_dotenv
from google import genai
from google.genai import types

load_dotenv()
logger = logging.getLogger(__name__)

GEMINI_API_KEY = os.getenv("GEMINI_API_KEY")
if not GEMINI_API_KEY:
    raise ValueError("GEMINI_API_KEY не знайдено в .env")

client = genai.Client(api_key=GEMINI_API_KEY)

SYSTEM_PROMPT = """Ти — віртуальний помічник з гри Valheim. Ти маєш доступ до бази знань з детальною інформацією про предмети, броню, зброю, босів та інші елементи гри.

## ТВОЯ ГОЛОВНА ЗАДАЧА
Допомагати гравцям стислими, корисними відповідями українською мовою.

## ЯК ВІДПОВІДАТИ

### Якщо в запиті є інформація з бази знань (у секції "Інформація з бази знань"):
- УВАЖНО прочитай надану інформацію
- Дай відповідь ПОВНОЦІННУ, ЯКА БАЗУЄТЬСЯ НА ЦІЙ ІНФОРМАЦІЇ
- Використай характеристики, опис та інші дані з бази
- Сформулюй відповідь природно, ніби пояснюєш другу

### Якщо інформація з бази знань ВІДСУТНЯ:
- Відповідай на основі СВОЇХ ЗНАНЬ про Valheim
- Якщо не впевнений — чесно скажи про це
- Якщо питання не про Valheim — ввічливо відмов

## ПРАВИЛА
1. Відповідай ТІЛЬКИ українською мовою
2. Будь стислим (3-4 речення)
3. НЕ перераховуй характеристики сухим списком — подай їх природно
4. Якщо база знань містить точні дані — ВИКОРИСТОВУЙ ЇХ, а не свої припущення
5. Якщо бази знань немає — покладайся на свої знання
6. Не вітайся, не прощайся, не додавай емодзі

## ПРИКЛАДИ

### Приклад 1 (з базою знань):
Інформація з бази: Бронзовий шолом, броня 8-14, вага 3, створюється у Кузні
Запит: "Бронзовий шолом"
Відповідь: Бронзовий шолом — надійний захист для ранньої гри. Він дає 8-14 одиниць броні та важить лише 3 одиниці. Створити його можна у Кузні після того, як виплавиш першу бронзу.

### Приклад 2 (без бази знань):
Запит: "Як швидко знайти залізо?"
Відповідь: Залізо найкраще шукати в затонулих склепах на Болотах. Для цього тобі знадобиться ключ, який випадає з боса Прадавнього. Візьми з собою кирку та трохи отрутостійкого зілля — воно дуже допоможе.

### Приклад 3 (не Valheim):
Запит: "Яка погода сьогодні?"
Відповідь: Я спеціалізуюсь лише на грі Valheim. Запитай мене про предмети, босів, біоми, крафт або тактики виживання!
"""


def ask_gemini_valheim(question: str, context: str = "") -> str:
    """
    Запитує Gemini з автоматичним перемиканням між моделями.
    """
    if context:
        user_content = f"""Інформація з бази знань:
{context}

Запитання гравця:
{question}

Дай стислу, корисну відповідь українською, використовуючи цю інформацію.
"""
    else:
        user_content = f"""Запитання гравця:
{question}

Дай стислу, корисну відповідь українською (3-4 речення). Якщо питання не про Valheim, ввічливо відмов.
"""

    models = ["gemini-2.5-flash", "gemini-2.0-flash", "gemini-2.0-flash-lite", "gemini-2.5-pro"]

    for model_name in models:
        try:
            response = client.models.generate_content(
                model=model_name,
                contents=user_content,
                config=types.GenerateContentConfig(
                    system_instruction=SYSTEM_PROMPT,
                    temperature=0.5,
                    max_output_tokens=300,
                ),
            )
            logger.info(f"Успішно використано модель: {model_name}")
            return response.text.strip()
        except Exception as e:
            logger.warning(f"Модель {model_name} недоступна: {e}")
            continue

    logger.error("Жодна модель Gemini не доступна")
    return "ERROR_API"